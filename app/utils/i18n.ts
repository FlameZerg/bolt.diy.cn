// ‰ªé localStorage Ëé∑ÂèñÂΩìÂâçËØ≠Ë®ÄÔºà‰∏éÁé∞ÊúâËÆæÁΩÆÁ≥ªÁªüÈõÜÊàêÔºâ
function getInitialLanguage(): Language {
    if (typeof window !== 'undefined') {
        try {
            const profile = localStorage.getItem('bolt_user_profile');
            if (profile) {
                const parsed = JSON.parse(profile);
                // È™åËØÅËØ≠Ë®ÄÊòØÂê¶ÊòØÊúâÊïàÁöÑLanguageÁ±ªÂûã
                const lang = parsed.language;
                if (typeof lang === 'string' && ['en', 'es', 'fr', 'de', 'it', 'pt', 'ru', 'zh', 'ja', 'ko'].includes(lang)) {
                    return lang as Language;
                }
            }
        } catch (e) {
            console.error('Error reading language from profile:', e);
        }
    }
    return 'en';
}

// ÂàõÂª∫ËØ≠Ë®ÄÁä∂ÊÄÅÂ≠òÂÇ®
export const languageStore = atom<Language>(getInitialLanguage());

// ÁõëÂê¨localStorageÂèòÂåñ‰ª•ÂêåÊ≠•ËØ≠Ë®ÄËÆæÁΩÆ
if (typeof window !== 'undefined') {
    window.addEventListener('storage', (event) => {
        if (event.key === 'bolt_user_profile' && event.newValue) {
            try {
                const parsed = JSON.parse(event.newValue);
                const lang = parsed.language;
                if (typeof lang === 'string' && ['en', 'es', 'fr', 'de', 'it', 'pt', 'ru', 'zh', 'ja', 'ko'].includes(lang)) {
                    languageStore.set(lang as Language);
                }
            } catch (e) {
                console.error('Error parsing profile update:', e);
            }
        }
    });

    // ÂàùÂßãÂêåÊ≠•
    const syncLanguage = () => {
        const lang = getInitialLanguage();
        if (lang !== languageStore.get()) {
            languageStore.set(lang);
        }
    };

    // ÂÆöÊúüÂêåÊ≠•ÔºàÂ§ÑÁêÜÂêå‰∏ÄÊ†áÁ≠æÈ°µÂÜÖÁöÑÊõ¥Êñ∞Ôºâ
    setInterval(syncLanguage, 1000);
}

